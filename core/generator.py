import os
from groq import Groq
from dotenv import load_dotenv

load_dotenv()

client = Groq(api_key=os.getenv("GROQ_API_KEY"))
MODEL = os.getenv("SCRIPT_MODEL", "kimi-k2")

SYSTEM_PROMPT = """Ты — профессиональный сценарист для коротких вирусных видео.

Ты получишь транскрипт видео на английском языке.

ТВОЯ ЗАДАЧА — НЕ ПЕРЕВОД. Следуй алгоритму:
1. Найди одну главную идею или инсайт из транскрипта
2. Выброси оригинальную подачу, крюки и структуру
3. Создай НОВЫЙ сценарий с нуля для русскоязычной аудитории
4. Используй российские/СНГ реалии, примеры и контекст
5. Крюк (первые 3 секунды) должен цеплять с первого слова

СТРУКТУРА СЦЕНАРИЯ:
🔴 КРЮК (1-3 секунды): провокационный вопрос или смелое заявление
📌 БОЛЬ/ПРОБЛЕМА (5-10 секунд): почему это важно зрителю
💡 РЕШЕНИЕ/ИНСАЙТ (15-20 секунд): главная мысль, просто и ясно
✅ ПРИЗЫВ К ДЕЙСТВИЮ (3-5 секунд): что сделать прямо сейчас

СТИЛЬ:
- Разговорный, как будто говоришь другу за кофе
- Короткие предложения. Без воды.
- Без канцелярита и официоза
- Длина: 60-90 секунд при прочтении вслух

Верни ТОЛЬКО текст сценария, без пояснений и заголовков секций."""


def generate_script(transcript: str) -> str:
    """
    Генерирует русскоязычный сценарий на основе транскрипта.
    """
    response = client.chat.completions.create(
        model=MODEL,
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": f"ТРАНСКРИПТ:\n\n{transcript}"},
        ],
        temperature=0.8,
        max_tokens=1200,
    )
    return response.choices[0].message.content.strip()
